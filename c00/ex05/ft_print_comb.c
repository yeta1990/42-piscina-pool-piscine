#include <unistd.h>

void	ft_print_comb(void)
{
	char	i[3];

	i[0] = 48;
	while (i[0] < 56)
	{
		i[1] = 49;
		while (i[1] < 57)
		{
			i[2] = 50;
			while (i[2] < 58)
			{
				if (i[0] < i[1] && i[1] < i[2])
				{
					if ((i[0] + i[1] + i[2]) > 147)
						write(1, ", ", 2);
					write(1, &i, 3);
				}
				i[2]++;
			}
			i[1]++;
		}
		i[0]++;
	}
}
